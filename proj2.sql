create table Participant (
    email varchar2(40),
    firstName varchar2(25),
    lastName varchar2(25),
    phone char(10),
    city varchar2(25),
    state char(2),
    hasTicket char(1),
    memberID number,
    yearJoined number(4),
    hasExpired char(1),
    numberOfArtEvents number,
    constraint email_PK primary key (email),
    constraint hasTicketVal check (hasTicket in ('Y', 'N')),
    constraint memberID_UQ unique (memberID),
    constraint hasExpiredVal check (hasExpired in ('Y', 'N', null))
);

create table Building (
    buildingName varchar2(25),
    street varchar2(25),
    city varchar2(25),
    state char(2),
    zipcode char(5),
    constraint buildingName_PK primary key (buildingName)
);

create table Gallery (
    galleryName varchar2(25),
    buildingName varchar2(25),
    maxCapacity number,
    constraint galleryBuilding_PK primary key (galleryName, buildingName),
    constraint buildingName_FK foreign key (buildingName) references Building(buildingName)
);

create table Artwork (
    artworkID number(3),
    category varchar2(25),
    title varchar2(40),
    price number,
    galleryName varchar2(25),
    buildingName varchar2(25),
    creatorEmail varchar2(40),
    winnerEmail varchar2(40),
    winOrder number(3),
    constraint artworkID_PK primary key (artworkID),
    constraint artworkCategory_UQ unique (artworkID, category), 
    constraint creatorEmail_FK foreign key(creatorEmail) references Participant(email),
    constraint winnerEmail_FK foreign key(winnerEmail) references Participant(email),
    constraint winOrder_UQ unique (winOrder)
);
-- Note: the following sequence starts with 1 and increments by 1 as a default
create sequence artworkID_seq;

create table TwoD (
    artworkID number(3),
    category varchar2(25) default 'TwoD' not null,
    medium varchar2(25),
    constraint TwoDartworkID_PK primary key (artworkID),
    constraint TwoDcategoryVal check (category in ('TwoD')),
    constraint TwoD_FK foreign key (artworkID, category) references Artwork(artworkID, category)
);

create table Pottery (
    artworkID number(3),
    category varchar2(25) default 'Pottery' not null,
    clayBody varchar2(25),
    constraint PotteryartworkID_PK primary key (artworkID),
    constraint PotterycategoryVal check (category in ('Pottery')),
    constraint Pottery_FK foreign key (artworkID, category) references Artwork(artworkID, category)
);

create table RankList (
    email varchar2(40),
    artworkID number(3),  
    rankOrder number(3) not null,
    constraint RankList_PK primary key (email, artworkID, rankOrder),
    constraint email_FK foreign key (email) References Participant(email),
    constraint artworkID_FK foreign key (artworkID) References Artwork(artworkID),
    constraint rankOrderVal check (rankOrder <= 20),
    constraint emailrankOrder_UQ unique (email, rankOrder)
);

